#This is how you define a single line string
a = "hello world"
print(a)

#This is how you define a multi-line string
b = """This is a long text This is a long text This is a long text
This is a long text This is a long text This is a long text
This is a long text This is a long text This is a long text"""

print(b)

#the characters in a string can be accessed by indexing
print(a[3])

#the part of a string can be obtained by using slicing
print(b[2:10])
print(b[2:20:2])